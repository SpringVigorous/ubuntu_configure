import pandas as pd

# 假设你有一个 DataFrame
df = pd.DataFrame({
    '季节': ['春', '夏'],
    '主图': ['图1', '图2'],
    '商家编码': ['A1', 'A2'],
    '颜色': ['红', '蓝'],
    '100': [10, 20],
    '110': [15, 25],
    '120': [12, 22],
    '总库存': [37, 57],
    '成本': [100, 200],
    '总金额': [3700, 5700]
})

# 要添加的新行数据
new_row = {'季节': '秋', '主图': '图3', '商家编码': 'A3', '颜色': '绿', '100': 30, '110': 35, '130': 32, '总库存': 97, '成本': 300, '总金额': 9700}

# 使用 loc 方法添加新行
df.loc[len(df)] = new_row

print(df)